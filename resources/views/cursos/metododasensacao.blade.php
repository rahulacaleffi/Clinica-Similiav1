                            </li>
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
                                <span class="topic">Módulo 3:</span> O Reino Vegetal e suas características: identificação dos temas da "canção vegetal”, compreensão da Sensação Vital de cada uma das 37 famílias vegetais estudadas. 2 casos completos ilustrativos.
                            </li>
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
                                <span class="topic">Módulo 4:</span> O Reino Animal e suas características: identificação dos temas da “canção animal”. Ordens e espécies. 8 Caixas - Métodos de tomada de caso (Sankaran, Jayesh e Dinesh), Sistemas informatizados, Repertorização estruturada e repertorização retrógrada.
                            </li>
                          </ul>
                        </p>

