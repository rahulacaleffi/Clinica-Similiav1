                                <span class="topic">Módulo 1:</span> Introdução ao Método da Sinergia, A Outra Canção, A Sensação Vital, os 7 Níveis de Experiência, Os 10 Miasmas e sua aplicação na prática clínica. 2 casos completos ilustrativos.
                            </li>
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
