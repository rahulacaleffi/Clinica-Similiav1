                        <h3 class="tagline">Dr. Rajan Sankaran</h3>

                        <p>O Dr. Rajan Sankaran é um conferencista internacionalmente aclamado, autor de dezenas de livros e diretor da "Other Song Academy" em Mumbai - Índia, para onde concorrem médicos e praticantes do mundo todo. É conhecido por suas ideias inovadoras em relação à Homeopatia, seja no campo da filosofia ou da matéria médica, seus penetrantes insights afetaram profundamente a forma de muitos homeopatas pensarem e clinicarem.</p>

                        <p>Seu método é intitulado por ele mesmo como “Método da Sinergia”  pois é composto por 3 partes, a saber:
                          <ul class="content-list">
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
                                <span class="topic">Gênio:</span> a “alma” do remédio, as características preponderantes que surgem da patogenesia e das curas clínicas observadas ao longo dos anos de uso dos medicamentos.
                            </li>
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
                                <span class="topic">Sintoma:</span> são os sintomas repertorizáveis, a síndrome mínima de valor máximo, o que é raro, estranho e peculiar, o que tem intensidade, persistência e individualidade no paciente.
                            </li>
                            <li>
                                <i class="fa fa-dot-circle-o"></i>
                                <span class="topic">Sistema:</span> esse sim é o método de trabalho de cada homeopata, nesse caso, o objetivo do curso, o método da SENSAÇÃO EM HOMEOPATIA.
                            </li>
                          </ul>
                        </p>

                        <br>

                        <h3 class="tagline">O Curso</h3>

                        <p>O Curso será ministrado em 4 módulos mensais, com duração de 16 horas cada, com conteúdos interligados e cumulativos. Cada módulo conta com a apresentação de 2 casos completos do mestre indiano, com a tomada do caso, a explicação do remédio pelo próprio Sankaran e os seguimentos. Os casos são legendados e com cura completa do paciente, em geral casos difíceis e através de remédios não comuns.</p>

                        <p>Após a apresentação, aproveitamos o conteúdo do caso para discorrermos sobre a filosofia e as técnicas utilizadas, tendo por objetivo construir o conhecimento prático completo para ser aplicado em nossas práticas clínicas.</p>

                        <p>Cada módulo terá um tema e um objetivo a ser alcançado, que no final formarão o corpo completo do Método da Sinergia.</p>

                        <br>

                        <h3 class="tagline">Objetivos do Curso</h3>

                        <p>Os objetivos do curso são, entre outros:
                          <ul class="content-list">
                            <li>
                                <i class="fa fa-check-square-o"></i>
                                Aquisição de habilidades e ferramentas técnicas para uma prescrição mais acertiva e consistente.
                            </li>
                            <li>
                                <i class="fa fa-check-square-o"></i>
                                Facilitar a resolução de casos tidos como difíceis pela abordagem tradicional pela exiguidade ou excesso de sintomas.
                            </li>
                            <li>
                                <i class="fa fa-check-square-o"></i>
                                Compreensão e prescrição segura de remédios tidos com “pequenos” ou pouco experimentados.
                            </li>
                            <li>
                                <i class="fa fa-check-square-o"></i>
                                Identificação e compreensão da “Outra Canção” ou “Sensação Vital” não humana e ligada à uma fonte pertencente a um dos reinos da natureza.
                            </li>
                            <li>
                                <i class="fa fa-check-square-o"></i>
                                Reconhecimento e elucidação clara dos  10 miasmas ou formas de enfermar.
                            </li>
                          </ul>
                        </p>

                        <br>

